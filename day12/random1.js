// Generate number between 1 to 6

let randomNumber = Math.random();
console.log("Random with * 6 = ", randomNumber * 6);

console.log(
  "Random with * 6 and then applying floor = ",
  Math.floor(randomNumber * 6)
);

console.log(
  "Random with * 6 and then applying floor then add 1 = ",
  Math.floor(randomNumber * 6) + 1
);
